public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        a += 5; // a = a + 5;

        System.out.println(a);

        b -= 1;
        System.out.println(b);
        b *= 15;
        System.out.println(b);
        b /= 3;
        System.out.println(b);
        b %= 7;
        System.out.println(b);
    }
}