public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;

        System.out.println("a == b : " + (a == b));
        System.out.println("a != b : " + (a != b));

        System.out.println("a > b : " + (a > b));
        System.out.println("a < b : " + (a < b));
        System.out.println("a >= 10 : " + (a >= 10));
        System.out.println("b <= 5 : " + (b <= 5));
    }
}